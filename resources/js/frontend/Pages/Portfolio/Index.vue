<script>
export default {
  name: "PortfolioIndex",
  mounted() {
    this.$nextTick(() => {
      // Initialize isotope portfolio filtering
      try {
        // Wait for jQuery and isotope to be available
        if (window.jQuery && window.jQuery.fn.isotope) {
          // Initialize the portfolio isotope
          const $portfolio = window.jQuery(
            "#portfolio-953311-141787 .portfolio_boxes"
          );

          if ($portfolio.length) {
            // Initialize isotope
            $portfolio.isotope({
              itemSelector: ".box",
              layoutMode: "masonry",
              masonry: {
                columnWidth: ".box",
              },
            });

            // Filter functionality
            window.jQuery(".filter_navigation a").on("click", function (e) {
              e.preventDefault();

              // Remove active class from all links
              window.jQuery(".filter_navigation a").removeClass("active");
              // Add active class to clicked link
              window.jQuery(this).addClass("active");

              // Get filter value
              const filterValue = window.jQuery(this).attr("data-filter");

              // Apply filter
              $portfolio.isotope({ filter: filterValue });
            });
          }
        } else {
          // If isotope is not available, try to load it via rt_run_isotope if available
          if (
            window.jQuery &&
            window.jQuery("#portfolio-953311-141787").length
          ) {
            if (
              typeof window.jQuery("#portfolio-953311-141787")
                .rt_run_isotope === "function"
            ) {
              window.jQuery("#portfolio-953311-141787").rt_run_isotope();
            }
          }
        }
      } catch (e) {
        console.warn("Portfolio isotope initialization failed:", e);
      }
    });
  },
};
</script>

